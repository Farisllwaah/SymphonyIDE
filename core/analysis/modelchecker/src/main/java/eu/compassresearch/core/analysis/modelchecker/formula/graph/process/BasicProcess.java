package eu.compassresearch.core.analysis.modelchecker.formula.graph.process;


public abstract class BasicProcess implements Process {
	
	
	
	@Override
	public boolean equals(Object obj) {
		if (this == obj)
			return true;
		if (obj == null)
			return false;
		if (getClass() != obj.getClass())
			return false;
		return true;
	}

}
